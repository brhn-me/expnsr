    @Test
    @Transactional
    void putExistingTransaction() throws Exception {
        // Initialize the database
        transactionRepository.saveAndFlush(transaction);

        int databaseSizeBeforeUpdate = transactionRepository.findAll().size();

        // Update the transaction
        Transaction updatedTransaction = transactionRepository.findById(transaction.getId()).get();
        // Disconnect from session so that the updates on updatedTransaction are not directly saved in db
        em.detach(updatedTransaction);
        updatedTransaction
            .date(UPDATED_DATE)
            .type(UPDATED_TYPE)
            .primaryCategory(UPDATED_PRIMARY_CATEGORY)
            .secondaryCategory(UPDATED_SECONDARY_CATEGORY)
            .amount(UPDATED_AMOUNT)
            .due(UPDATED_DUE)
            .title(UPDATED_TITLE)
            .description(UPDATED_DESCRIPTION)
            .tags(UPDATED_TAGS);
        TransactionDTO transactionDTO = transactionMapper.toDto(updatedTransaction);

        restTransactionMockMvc
            .perform(
                put(ENTITY_API_URL_ID, transactionDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(transactionDTO))
            )
            .andExpect(status().isOk());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeUpdate);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getDate()).isEqualTo(UPDATED_DATE);
        assertThat(testTransaction.getType()).isEqualTo(UPDATED_TYPE);
        assertThat(testTransaction.getPrimaryCategory()).isEqualTo(UPDATED_PRIMARY_CATEGORY);
        assertThat(testTransaction.getSecondaryCategory()).isEqualTo(UPDATED_SECONDARY_CATEGORY);
        assertThat(testTransaction.getAmount()).isEqualByComparingTo(UPDATED_AMOUNT);
        assertThat(testTransaction.getDue()).isEqualByComparingTo(UPDATED_DUE);
        assertThat(testTransaction.getTitle()).isEqualTo(UPDATED_TITLE);
        assertThat(testTransaction.getDescription()).isEqualTo(UPDATED_DESCRIPTION);
        assertThat(testTransaction.getTags()).isEqualTo(UPDATED_TAGS);
    }

